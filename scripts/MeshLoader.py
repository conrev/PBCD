    # format f vertexId0/vertexTexture0/vertexNormal0 vertexId1/vertexTexture1/vertexNormal1 vertexId2/vertexTexture2/vertexNormal2 ..
    vertexId, vertexTexture, vertexNormal = str.split("/")
    return int(vertexId)-1

